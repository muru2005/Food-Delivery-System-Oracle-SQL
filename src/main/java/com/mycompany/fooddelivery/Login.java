    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    String username = jTextField1.getText().trim();
    String password = jTextField2.getText().trim();
    String sql = "SELECT user_type,user_id FROM credential WHERE username = ? AND password = ?";
    String user_id[]={null};
    try {
        ps = con.prepareStatement(sql);
        ps.setString(1, username);
        ps.setString(2, password);
        rs = ps.executeQuery();
        
        if (rs.next()) {
            String userType = rs.getString("user_type");
            JOptionPane.showMessageDialog(this, "Login successful! User type: " + userType);
            user_id[0]=rs.getString("user_id");
            System.out.println(userType);
            System.out.println(user_id[0]);
            // Open the appropriate home screen depending on user type
            if (userType.equalsIgnoreCase("restaurant")) {
                String sql3="select name from restaurant_loc where rest_id=?";
               try{
                ps1=con.prepareStatement(sql3);
                ps1.setString(1,user_id[0]);
                rs1=ps1.executeQuery();
                if(rs1.next()){
                  String restname=rs1.getString("name");
                   System.out.println(restname);
                  String sql4="select menu_id from restaurant where restaurantname=?";
                  try{
                      ps2=con.prepareStatement(sql4);
                      ps2.setString(1,restname);
                     
                       rs2=ps2.executeQuery();
                      if(rs2.next()){
                         String menuid=rs2.getString("menu_id");
                         String []temp={menuid};
                         MenuAlter altercall=new MenuAlter(temp);
                         altercall.setVisible(true);
                         this.dispose();
                      }
                  }catch(Exception r){
                     System.out.println(r);
                  }
                }
               }catch(Exception q){
                 System.out.println(q);
               }
                
            } else if (userType.equalsIgnoreCase("user")) {
                 String  uname[]={username};
                RestaurantSearch registerPage = new RestaurantSearch(uname);
               registerPage.setVisible(true);
               this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Unknown user type!");
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password!");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }
  
}

    
    
